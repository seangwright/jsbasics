// Values have properties

var x = 'abc';

console.log("Length of variable x = " + x.length);

console.log("Length of string 'abc' = " + 'abc'.length);

var y = 5;

console.log("Variable y with value 5 as a string using toString() = " + y.toString());

console.log("Value 5 parsed and then toString() = " + parseInt(5).toString());

// You can assign values to properties but not to the properties of values :)
